package com.itheima.Variable;

public class VariableDemo {
    public static void main(String[] args) {
        //变量定义格式 数据类型 变量名（第一个字母小写）=数据；
        int age=23;
        System.out.println(age);

        System.out.println(23);

        double money=46.3;
        System.out.println(money);

    }
}
